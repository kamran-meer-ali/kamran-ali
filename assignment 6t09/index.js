 let a = 10;
 document.write("The value of a is: " + a + "<br><br>");
 document.write("...............................................<br><br>");
 
 // Pre-increment
 document.write("The value of ++a is: " + (++a) + "<br>");
 document.write("Now the value of a is: " + a + "<br><br>");
 
 // Post-increment
 document.write("The value of a++ is: " + (a++) + "<br>");
 document.write("Now the value of a is: " + a + "<br><br>");
 
 // Pre-decrement
 document.write("The value of --a is: " + (--a) + "<br>");
 document.write("Now the value of a is: " + a + "<br><br>");
 
 // Post-decrement
 document.write("The value of a-- is: " + (a--) + "<br>");
 document.write("Now the value of a is: " + a + "<br>");

